							<h2 class="theme">Александр Мясников: Я знаю, как безопасно похудеть на 30 кг за месяц в домашних условиях</h2>
							<p><b>Сегодня кандидат медицинских наук (Россия), доктор медицины и врач высшей категории (США) Александр Мясников расскажет, как современные достижения медицины позволяют за месяц в домашних условиях избавиться от 30 кг лишнего веса без всякого вреда для здоровья</b></p>
							<p><b>Александр Мясников:</b> Итак, отчего же у человека возникает лишний вес, то есть его масса больше  той, которую рекомендуют врачи и которая отражена в таблице  так называемого индекса массы тела. Причин всего две. Первая – банальное переедание. То есть мы съедаем калорий больше, чем расходуем. Поэтому сейчас врачи повсеместно рекомендуют отказываться от высококалорийной пищи и побольше заниматься физической деятельностью. Вторая причина нарушение обмена веществ. То есть у человека в организме начинают идти биохимические реакции не так, как они должны идти.</p>
							<div style="text-align: center;padding-bottom: 15px">
								<img src="img/1.jpg" alt="" style="max-width: 100%;" />
							</div>
							<p>В результате вместо того, чтобы перерабатывать еду на получение энергии, которую можно использовать для физической и умственной активности, наш организм превращает ее в жир. Как результат – человек становится вялым, как физически, так и умственно. У него растет вес, а внутренние органы буквально купаются в жире. Проблема усугубляется еще и тем, что переедание часто, в конце концов, приводит к нарушению  обмена веществ. То есть мы полнеем и потому что передаем и потому что у нас нарушен обмен веществ. А лишний вес – это главный провокатор сердечно-сосудистых заболеваний, заболеваний пищеварительной системы и ряда других опасных заболеваний. Как показали исследования, тридцать килограмм лишнего веса в среднем уменьшают продолжительность жизни на 15 лет! Пятнадцать лет человек вычеркивает из своей жизни. Природа нас наказывает как за тяжкое преступление – целых 15 лет. </p>
							<p>Почему же люди, в большинстве свое не могут победить лишние килограммы? Причин несколько. Во-первых, на полках наших магазинов очень много вредной жирной, но при этом вкусной пищи. Во-вторых, у среднестатистического человека остается очень мало времени и сил на физические упражнения. Уж в слишком напряженном ритме мы живем, чтобы добиться успеха в жизни.</p>
							<div style="text-align: center;padding-bottom: 15px">
								<img src="img/2.jpg" alt="" style="max-width: 100%;" />
							</div>
							<p>Какие существуют популярные способы похудения? В принципе, их два. Первый – применение всевозможных диет. Второй – использование различных препаратов для  похудения. В чем основной недостаток первого способа. Любая диета, подчеркиваю, любая, предполагает определённые ограничения по еде. То есть организм лишается тех или иных нужных ему веществ – витаминов, микроэлементов, аминокислот и т.д. А это вредит здоровью.</p>
							<p>Теперь о препаратах для похудения. В большинстве своем они либо уменьшают аппетит, что опять-таки приводит к неполноценному питанию. Либо при помощи различных синтетических веществ, то есть в принципе чуждых нашему организму,  выводят из организма еду, не давая ей усваиваться. То есть мы видим все то же неполноценное питание и дефицит поступления нужных веществ в организм. </p>
							<div style="text-align: center;padding-bottom: 15px">
								<img src="img/3.jpg" alt="" style="max-width: 100%;" />
							</div>
							<p>Медики уже давно поняли, что нужно, чтобы человек имел нормальный вес. Их рекомендации представлены на этой таблице. Как видите, в принципе, ничего сложного. Но есть одно большое НО. Современному человеку их очень трудно выполнить. То есть получается, что с одной стороны существующие диеты и препараты неэффективны, а то опасны для организма. А с другой – рекомендации, которые помогут на сто процентов в реальных условиях выполнить могут только считанные единицы людей.</p>
							<p>А теперь хорошая новость! Отлично понимая весь этот комплекс проблем в США, под эгидой Американской ассоциации диетологов был разработан революционный препарат для похудения нового поколения <a href="">SlimBiotic</a>. Его принципиальное отличие от других средств для похудения – его принцип действия основан на естественном процессе сжигания жира, который существует в нашем организме.</p>
							<p>Пару слов о нем. Диетологи обнаружили, что если в рационе питания углеводы заменить жирами, то для их сжигания печень начинает вырабатывать особые вещества – кетоновые тела. На этом основана знаменитая кетоновая диета, имеющая сейчас десятки миллионов приверженцев во всем мире.  Но, несмотря на свою эффективность и связанную с этим популярность, эта диета имеет несколько существенных недостатков. Во-первых, углеводы тоже необходимы организму. А при этой диете они не поступают. Во-вторых, увеличивается нагрузка на печень,  ведь в организм начинает поступать значительно больше жира. Препарат <a href="">SlimBiotic</a> лишен этих недостатков. Он не требует отказа от углеводов и увеличенного потребления жиров.</p>
							<p>Проведенные в США испытания выявили еще ряд достоинств нового препарата:</p>
							<p>
								1.	Он абсолютно безопасен и допускает потерю веса даже со скоростью 45 кг в месяц.<br />
								2.	Он позволяет вести обычный образ жизни. То есть  не нужно садиться на диеты и идти в спортзал.
							</p>
							<p>Другими словами <a href="">SlimBiotic</a>  идеально подходит для современного человека.</p>
							<p>А теперь другая хорошая новость! Три месяца назад в Институте Диетологии были начаты комплексные испытания препарата <a href="">SlimBiotic</a>. Они полностью подтвердили выводы, которые были сделаны американскими учеными. В группе из 367 человек, страдающих ожирением 2-4 степени, в результате двухмесячного приема <a href="">SlimBiotic</a>  у всех испытуемых было зафиксировано значительное снижение веса. Минимум на 18 кг. У всех испытуемых вес стал соответствовать физиологической норме.  Абсолютный результат! А у испытуемых, страдающих ожирением 4 степени, было зафиксировано уменьшение веса  на 30 – 37 кг в месяц. Особо хорошо подчеркнуть, что все испытуемые в ходе эксперимента были под тщательным медицинским наблюдением. И никаких отрицательных моментов, связанных с быстрым похудением обнаружено не было.</p>
